// bitwise or opretor
#include<iostream>
using namespace std;

// 1. bitwise &
// 2, bitwise |
// 3. bitwise ~
// 4. bitwise xor ^

int main(){
    cout<<(10&4)<<endl;
    cout<<(11&9)<<endl;
    cout<<(10|4)<<endl;
    cout<<(11|9)<<endl;
    cout<<(~10)<<endl; //negation of a no. is -(no.+1)
    cout<<(10^4)<<endl;
    cout<<(12^5)<<endl;//xor example
}  